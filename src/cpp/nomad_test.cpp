uint8_t crc8(const uint8_t* data, uint8_t len) {
    uint8_t crc = 0;
    for (uint8_t i = 0; i < len; i++) crc = crc_table[crc ^ data[i]];
    return crc;
}

int main() {
    cout << "========================================" << endl;
    cout << "   RadioMaster Nomad Link Tester        " << endl;
    cout << "========================================" << endl;
    cout << "Select Baud Rate to Test:" << endl;
    cout << "1) 400,000   (Safe - 250Hz)" << endl;
    cout << "2) 921,600   (Fast - 500Hz)" << endl;
    cout << "3) 1,870,000 (Extreme - 1000Hz)" << endl;
    cout << "4) 3,750,000 (Ludicrous - 1000Hz+)" << endl;
    cout << "\nEnter choice (1-4): ";

    int choice;
    cin >> choice;

    int target_baud = 400000;
    if (choice == 2) target_baud = 921600;
    else if (choice == 3) target_baud = 1870000;
    else if (choice == 4) target_baud = 3750000;

    int fd = open(PORT, O_RDWR | O_NOCTTY);
    if (fd < 0) { perror("Open failed"); return 1; }

    struct termios2 tty;
    ioctl(fd, TCGETS2, &tty);
    tty.c_cflag &= ~CBAUD;
    tty.c_cflag |= BOTHER;
    tty.c_ispeed = target_baud;
    tty.c_ospeed = target_baud;
    tty.c_cflag = (tty.c_cflag & ~CSIZE) | CS8 | CLOCAL | CREAD;
    tty.c_cflag &= ~(PARENB | CSTOPB);
    tty.c_iflag &= ~(IXON | IXOFF | IXANY);
    tty.c_lflag = 0; tty.c_oflag = 0;
    ioctl(fd, TCSETS2, &tty);

    cout << "\nStarting 5000 packet test at " << target_baud << " bps..." << endl;

    long long send_count = 0, valid_count = 0, crc_errors = 0;
    uint8_t packet[26];

    for(int i=0; i<5000; i++) {
        packet[0] = 0xEE; packet[1] = 0x18; packet[2] = 0x16;
        for(int j=3; j<25; j++) packet[j] = (uint8_t)(send_count + j);
        packet[25] = crc8(&packet[2], 23);

        write(fd, packet, 26);
        send_count++;

        uint8_t rx_buf[26];
        int bytes_read = 0;
        auto wait_start = steady_clock::now();
        while(bytes_read < 26 && duration_cast<milliseconds>(steady_clock::now() - wait_start).count() < 10) {
            int n = read(fd, rx_buf + bytes_read, 26 - bytes_read);
            if (n > 0) bytes_read += n;
        }

        if (bytes_read == 26) {
            if (memcmp(rx_buf, packet, 26) == 0) valid_count++;
            else if (rx_buf[25] != crc8(&rx_buf[2], 23)) crc_errors++;
        }
        usleep(100); 
    }

    cout << "---------------------------------" << endl;
    cout << "Accuracy: " << fixed << setprecision(2) << (valid_count * 100.0 / send_count) << "%" << endl;
    cout << "CRC Failures: " << crc_errors << endl;
    
    if (valid_count == send_count) cout << "RESULT: PERFECT." << endl;
    else cout << "RESULT: SIGNAL DEGRADED." << endl;

    close(fd);
    return 0;
}
