// crsf_sender.cpp - Real CRSF packet sender for RadioMaster Nomad

#include <iostream>
#include <fcntl.h>
#include <unistd.h>
#include <termios.h>
#include <cstring>
#include <chrono>

class CRSFSender {
private:
    int fd = -1;
    const char* port = "/dev/ttyACM0";   // Default for Nomad (change when plugged in)
    const int baud = 400000;             // Official recommended CRSF baud rate

    // Official CRSF CRC8 table
    static const uint8_t crc8_table[256];

    uint8_t crc8(const uint8_t* data, uint8_t len) {
        uint8_t crc = 0;
        for (uint8_t i = 0; i < len; i++) {
            crc = crc8_table[crc ^ data[i]];
        }
        return crc;
    }

public:
    bool begin() {
        fd = open(port, O_RDWR | O_NOCTTY | O_NDELAY);
        if (fd < 0) {
            std::cerr << "[CRSF] Failed to open " << port << " (will retry on next send)" << std::endl;
            return false;
        }

        struct termios tty;
        tcgetattr(fd, &tty);
        cfsetospeed(&tty, baud);
        cfsetispeed(&tty, baud);
        tty.c_cflag = (tty.c_cflag & ~CSIZE) | CS8;
        tty.c_cflag |= CLOCAL | CREAD;
        tty.c_cflag &= ~(PARENB | CSTOPB | CRTSCTS);
        tty.c_iflag &= ~(IXON | IXOFF | IXANY);
        tty.c_lflag &= ~(ICANON | ECHO | ECHOE | ISIG);
        tty.c_oflag &= ~OPOST;
        tcsetattr(fd, TCSANOW, &tty);

        std::cout << "[CRSF] Connected to Nomad on " << port << " @ " << baud << " baud" << std::endl;
        return true;
    }

    // Send 16 channels in real CRSF format
    void send_tuned_data(int lx, int ly, int rx, int ry, int l2, int r2) {
        if (fd < 0) return;

        uint8_t packet[26] = {0};

        packet[0] = 0xC8;        // Sync byte
        packet[1] = 24;          // Length
        packet[2] = 0x16;        // RC Channels Packed

        // Convert tuned values to CRSF range (0-2047, center = 1024)
        int ch[16] = {0};
        ch[0] = lx + 1024;   // Roll
        ch[1] = ly + 1024;   // Pitch
        ch[2] = ry + 1024;   // Throttle
        ch[3] = rx + 1024;   // Yaw
        ch[4] = l2 + 1024;   // L2 Trigger
        ch[5] = r2 + 1024;   // R2 Trigger
        // ch[6..15] = switches later if needed

        // Clamp to valid range
        for (int i = 0; i < 16; i++) {
            if (ch[i] < 0) ch[i] = 0;
            if (ch[i] > 2047) ch[i] = 2047;
        }

        // Pack 11-bit channels into 22 bytes
        uint8_t* p = &packet[3];
        for (int i = 0; i < 16; i++) {
            int byte_index = i * 11 / 8;
            int bit_offset = i * 11 % 8;
            p[byte_index] |= (ch[i] << bit_offset) & 0xFF;
            p[byte_index + 1] |= (ch[i] >> (8 - bit_offset)) & 0xFF;
        }

        // Add CRC
        packet[25] = crc8(&packet[2], 23);

        write(fd, packet, 26);
    }

    void close_port() {
        if (fd >= 0) {
            ::close(fd);
            fd = -1;
        }
    }
};

